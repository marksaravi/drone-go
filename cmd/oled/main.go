func main() {
	// Make sure periph is initialized.
	if _, err := host.Init(); err != nil {
		log.Fatal(err)
	}

	// Use i2creg I²C bus registry to find the first available I²C bus.
	b, err := i2creg.Open("")
	if err != nil {
		log.Fatal(err)
	}
	defer b.Close()
	ops := ssd1306.DefaultOpts
	ops.H = 64
	dev, err := ssd1306.NewI2C(b, &ops)
	if err != nil {
		log.Fatalf("failed to initialize ssd1306: %v", err)
	}

	img := image1bit.NewVerticalLSB(dev.Bounds())
	if err := dev.Draw(dev.Bounds(), img, image.Point{}); err != nil {
		log.Fatal(err)
	}

	const charW = 6
	const charH = 13
	const SCALE = 2
	// addChar(dev, img, 48, charW, charH, SCALE, 0, 0)
	// addChar(dev, img, 49, charW, charH, SCALE, 2, 0)
	addMyChar(dev, img, 49)
	if err := dev.Draw(dev.Bounds(), img, image.Point{}); err != nil {
		log.Fatal(err)
	}
}

func addChar(dev *ssd1306.Dev, img *image1bit.VerticalLSB, charCode int, charW, charH, scale, x, y int) {
	charOffset := charCode - 0x20
	char := characters[charOffset*charW*charH : (charOffset+1)*charW*charH]
	offset := image.Point{x * charW, y * charH}
	for x := 0; x < charW; x++ {
		for y := 0; y < charH; y++ {
			if char[x+y*charW] > 0 {
				for s := 1; s <= scale; s++ {
					setPixel(x*scale+s+offset.X, y*scale+s+offset.Y, dev.Bounds(), img)
				}

			}

		}
	}

}

func addMyChar(dev *ssd1306.Dev, img *image1bit.VerticalLSB, charCode int) {
	const CHAR_W = 15
	const CHAR_H = 26

	char := fontData[charCode-48]
	for y := 0; y < CHAR_H; y++ {
		for x := 0; x < CHAR_W; x++ {
			if char[y][x] > 0 {
				setPixel(x, y, dev.Bounds(), img)
			}
		}
	}

}

func setPixel(x, y int, bounds image.Rectangle, img *image1bit.VerticalLSB) {
	absIndex := (y/8)*bounds.Dx() + x
	maskIndex := y % 8
	maskValue := byte(1) << byte(maskIndex)

	img.Pix[absIndex] = img.Pix[absIndex] | maskValue
}

